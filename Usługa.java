public class Usluga {

	private string idUslugi;
	private string typ;
	private string szczegoly;

	public string getIdUslugi() {
		return this.idUslugi;
	}

	public void setIdUslugi(string idUslugi) {
		this.idUslugi = idUslugi;
	}

	public string getTyp() {
		return this.typ;
	}

	public void setTyp(string typ) {
		this.typ = typ;
	}

	public string getSzczegoly() {
		return this.szczegoly;
	}

	public void setSzczegoly(string szczegoly) {
		this.szczegoly = szczegoly;
	}

}