public class Uzytkownik {

	public string idUzytkownika;
	public string imie;
	public string daneKontaktowe;
	public string nazwisko;

	public string getIdUzytkownika() {
		return this.idUzytkownika;
	}

	public void setIdUzytkownika(string idUzytkownika) {
		this.idUzytkownika = idUzytkownika;
	}

	public string getImie() {
		return this.imie;
	}

	public void setImie(string imie) {
		this.imie = imie;
	}

	public string getDaneKontaktowe() {
		return this.daneKontaktowe;
	}

	public void setDaneKontaktowe(string daneKontaktowe) {
		this.daneKontaktowe = daneKontaktowe;
	}

	public string getNazwisko() {
		return this.nazwisko;
	}

	public void setNazwisko(string nazwisko) {
		this.nazwisko = nazwisko;
	}

}